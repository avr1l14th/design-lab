const FILLS: string[] = [
  "#0138C7",
  "#9C373C",
  "#C65528",
  ...PHOTO_FILES.map((_, i) => `url(#lab-logo-p${i})`),
];

const FRAME_MS = 120;

export function LabLogo() {
  const [index, setIndex] = useState(0);
  const [hover, setHover] = useState(false);
  const timerRef = useRef<ReturnType<typeof setInterval> | null>(null);

  useEffect(() => {
    if (!hover) {
      if (timerRef.current) clearInterval(timerRef.current);
      timerRef.current = null;
      setIndex(0);
      return;
    }
    timerRef.current = setInterval(() => {
      setIndex((i) => (i + 1) % FILLS.length);
    }, FRAME_MS);
    return () => {
      if (timerRef.current) clearInterval(timerRef.current);
    };
  }, [hover]);

  return (
    <svg
      viewBox="0 0 32 32"
      width={32}
      height={32}
      xmlns="http://www.w3.org/2000/svg"
      onMouseEnter={() => setHover(true)}
      onMouseLeave={() => setHover(false)}
      className="block h-[32px] w-[32px] shrink-0 select-none"
      aria-label="mymeet.ai"
      role="img"
    >
      <defs>
        {PHOTO_FILES.map((file, i) => (
          <pattern
            key={file}
            id={`lab-logo-p${i}`}
            patternUnits="objectBoundingBox"
            width={1}
            height={1}
          >
            <image
              href={`${BASE}/lab/photos/${file}`}
              x={0}
              y={0}
              width={32}
              height={32}
              preserveAspectRatio="xMidYMid slice"
            />
          </pattern>
        ))}
      </defs>
      <path d={PATH_D} fillRule="evenodd" fill={FILLS[index]} />
    </svg>
  );
}
